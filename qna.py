import streamlit as st


def render_qna():
    st.subheader("Q&A / 도움말")
    st.caption("이 도구가 무엇을 잘하고, 어떤 입력에 적합한지 빠르게 확인할 수 있습니다.")

    items = [
        (
            "1. Clean Text는 어떤 도구인가요?",
            """
Clean Text는 복잡하고 비구조화된 의료 텍스트를 더 읽기 쉽게 정리하고,
일부 검사 결과를 표 형태로 구조화해 엑셀로 내보낼 수 있도록 돕는 도구입니다.

주요 기능은 두 가지입니다.

- 텍스트 클리너: EMR 텍스트, 오더/노트, 보고서형 텍스트를 더 읽기 쉽게 정리
- 테이블 변환기: Lab 결과를 구조화된 표로 변환하여 엑셀로 내보내기
""".strip(),
        ),
        (
            "2. 어떤 입력에 가장 잘 맞나요?",
            """
가장 잘 맞는 입력은 다음과 같습니다.

- CBC, chemistry, blood gas, coagulation 등 정량 검사 결과
- urine dipstick, urine microscopy 등 일부 정성/반정량 검사 결과
- 복붙 후 공백, 줄바꿈, 섹션이 흐트러진 EMR 텍스트

즉, row-based 검사 결과와 정리 가능한 의료 텍스트에 가장 적합합니다.
""".strip(),
        ),
        (
            "3. 어떤 입력은 테이블 변환기에 적합하지 않나요?",
            """
다음과 같은 입력은 테이블 변환기보다 텍스트 클리너 사용을 권장합니다.

- 내시경 결과
- 영상 판독 결과
- 병리 보고서
- 장문 서술형 판독문
- narrative 중심의 미생물/혈액은행 결과

이런 결과는 한 줄짜리 검사값이 아니라 보고서형 텍스트에 가깝기 때문에,
표로 억지로 구조화하기보다 텍스트 정리 후 직접 확인하는 방식이 더 적합합니다.
""".strip(),
        ),
        (
            "4. 테이블 변환기는 어떤 방식으로 결과를 나누나요?",
            """
테이블 변환기는 입력을 크게 세 가지로 구분합니다.

- 정량 검사: 숫자값, 단위, 참고치가 비교적 명확한 검사
- 정성/반정량 검사: Negative, Positive, Trace, 1+, 2+, cells/HPF 같은 결과
- 보고서형 검사: 내시경, 영상, 병리, 장문 판독 결과

보고서형으로 분류된 내용은 표 변환 대신 별도로 표시되며,
텍스트 클리너 사용을 권장합니다.
""".strip(),
        ),
        (
            "5. 정량 검사는 어떻게 표시되나요?",
            """
정량 검사는 가능한 경우 다음 구조로 정리됩니다.

- 검사명
- 결과값
- 단위
- 참고치

EMR 원문에서 ▲/▼가 포함된 경우, 결과값에 그대로 반영됩니다.
엑셀 출력에서도 상승/하강 표시를 색으로 구분해 보여줄 수 있습니다.
""".strip(),
        ),
        (
            "6. 정성 검사는 어떻게 표시되나요?",
            """
정성/반정량 결과는 결과값과 참고치를 바탕으로 판정합니다.

예를 들어:
- Positive, Trace, 1+, 2+, 3+ 등은 비정상으로 해석될 수 있습니다
- Negative 기준에서 벗어나는 결과는 강조 표시될 수 있습니다
- 0~3 cells/HPF, <50, >50, 30~40 같은 참고치가 있으면 그 기준에 따라 비교합니다

즉, 정성검사도 가능한 범위 내에서 이상 여부를 자동 표시합니다.
""".strip(),
        ),
        (
            "7. 결과가 항상 완벽하게 파싱되나요?",
            """
아닙니다. 이 도구는 규칙 기반 parser이기 때문에 입력 형식에 따라 일부 항목은 완전히 파싱되지 않을 수 있습니다.

특히 아래 경우는 주의가 필요합니다.

- 형식이 많이 흐트러진 텍스트
- 단위와 참고치가 한 줄에 붙어 있는 경우
- 장문 판독 결과
- 병원/부서별로 포맷이 다른 결과지

따라서 중요한 데이터는 원문과 함께 확인하는 것을 권장합니다.
""".strip(),
        ),
        (
            "8. 엑셀 시트는 어떻게 구성되나요?",
            """
입력 내용에 따라 다음과 같은 시트가 생성될 수 있습니다.

- Parsed_Labs: 정량 검사 결과
- Qualitative_Results: 정성/반정량 검사 결과
- Grouped_Tables: 정량 결과를 보기 좋게 그룹화한 시트
- Grouped_Qualitative: 정성 결과를 그룹화한 시트
- Original_Text: 원문 보관
- Unparsed: 파싱되지 않은 줄
- Ignored_Reports: 보고서형으로 분류되어 표 변환에서 제외된 텍스트

즉 결과를 바로 쓰는 용도뿐 아니라,
누락된 줄이나 원문을 다시 확인하는 용도로도 활용할 수 있습니다.
""".strip(),
        ),
        (
            "9. 텍스트 클리너는 어떤 역할을 하나요?",
            """
텍스트 클리너는 표 변환용이 아니라 가독성 개선용입니다.

예를 들어:
- 전각 공백 제거
- 불필요한 연속 공백 정리
- 줄바꿈/빈 줄 정리
- SOAP / Problem list / note block 구조 정리

특히 표로 바꾸기 어려운 보고서형 텍스트는
테이블 변환기보다 텍스트 클리너에서 먼저 다듬는 것이 더 유용합니다.
""".strip(),
        ),
        (
            "10. 어떤 모드부터 써보는 게 좋나요?",
            """
처음 사용할 때는 아래처럼 시작하는 것을 추천합니다.

- 표로 만들고 싶은 검사 결과 → 테이블 변환기
- 그냥 읽기 좋게 정리하고 싶은 텍스트 → 텍스트 클리너
- 내시경/영상/병리처럼 보고서형 결과 → 텍스트 클리너

즉, 숫자 중심 결과는 테이블 변환기,
서술형 결과는 텍스트 클리너라고 생각하시면 가장 편합니다.
""".strip(),
        ),
        (
            "11. 결과가 이상하게 나오면 어떻게 해야 하나요?",
            """
다음 순서로 확인하면 좋습니다.

1. 원문 형식이 지나치게 깨져 있지 않은지 확인
2. 보고서형 결과를 테이블 변환기에 넣은 것은 아닌지 확인
3. Unparsed 또는 Ignored_Reports 시트를 확인
4. 필요한 경우 원문 일부를 텍스트 클리너에서 먼저 정리한 뒤 다시 시도

즉, 파싱 실패는 항상 오류라기보다 입력 형식과 도구의 목적이 맞지 않는 경우일 수도 있습니다.
""".strip(),
        ),
        (
            "12. 이 도구를 어떻게 생각하면 가장 좋나요?",
            """
이 도구는 의료 텍스트를 더 빠르게 읽고 정리하기 위한 보조 도구입니다.
완전한 EMR parser나 의료 판독 엔진이라기보다,
반복되는 수작업을 줄여주는 실용적인 workflow tool에 가깝습니다.

따라서 가장 좋은 사용법은

- 원문 확인을 완전히 대체하지 않고
- 정리, 구조화, 엑셀 변환을 빠르게 보조하는 용도

로 활용하는 것입니다.
""".strip(),
        ),
    ]

    for title, body in items:
        with st.expander(title):
            st.markdown(body)
